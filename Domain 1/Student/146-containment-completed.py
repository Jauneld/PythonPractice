#Important to know if a value is located some where 
#Keyword
#"in": is the value asked for in the data type
#"not in": is the value not in the data type

ancient_civilizations = ['Mesopotamia','Egypt','Indus Valley','China']

print('Egypt' in ancient_civilizations)
print('Japan' in ancient_civilizations)
print('Japan' not in ancient_civilizations)
print('China' in ancient_civilizations)
