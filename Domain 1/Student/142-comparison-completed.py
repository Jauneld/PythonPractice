#Symbols 
#> :greater than
#< :less than
#== :equal to 
#>= :greater than or equal to 
#<= :less than or equal to 
#!= :not equal to

player1_score = 300
player2_score = 400
player3_score = 300

print(player1_score == player2_score)#False
print(player1_score == player3_score)#True
print(player1_score != player2_score)#True
print(player1_score < player3_score)#False
print(player1_score > player2_score)#False
print(player1_score >= player2_score)#False
print(player1_score <= player2_score)#True
